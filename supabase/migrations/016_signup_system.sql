-- 5. UPDATE EXISTING SELECT POLICIES TO BLOCK PENDING USERS
-- ============================================================
-- Pending users can authenticate but should not see scheduling
-- data (mass_events, booking_slots, choir_signups) until approved.
-- Strategy: drop the broad "authenticated" SELECT policy and replace
-- it with one that requires is_active_user() OR admin role.
-- Admin check is explicit so that pending admins (rare edge case)
-- still get access.

-- Helper: admin check subquery used repeatedly below
-- (inline — no separate function needed)

-- ---- mass_events ----
DROP POLICY IF EXISTS "Anyone authenticated can view mass events" ON public.mass_events;
DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE tablename = 'mass_events' AND policyname = 'Active users can view mass events'
  ) THEN
    CREATE POLICY "Active users can view mass events"
      ON public.mass_events FOR SELECT
      USING (
        public.is_active_user(auth.uid())
        OR EXISTS (
          SELECT 1 FROM public.profiles
          WHERE profiles.id = auth.uid() AND profiles.role = 'admin'
        )
      );
  END IF;
END $$;
