-- 3. INVITATIONS TABLE
-- ============================================================
-- Admin-issued invite codes. A new volunteer redeems a code
-- during signup to get a pre-approved account.

CREATE TABLE IF NOT EXISTS public.invitations (
  id          uuid        PRIMARY KEY DEFAULT gen_random_uuid(),
  code        text        UNIQUE NOT NULL,          -- 8-char alphanumeric (generated by app)
  invited_by  uuid        REFERENCES public.profiles(id) ON DELETE SET NULL,
  invited_phone text,                               -- if delivered via SMS
  invited_email text,                               -- if delivered via email
  ensemble    text,                                 -- pre-fill ensemble on signup form
  status      text        NOT NULL DEFAULT 'pending'
              CHECK (status IN ('pending', 'claimed', 'expired')),
  claimed_by  uuid        REFERENCES public.profiles(id) ON DELETE SET NULL,
  claimed_at  timestamptz,
  expires_at  timestamptz NOT NULL DEFAULT (now() + interval '30 days'),
  created_at  timestamptz NOT NULL DEFAULT now()
);
ALTER TABLE public.invitations ENABLE ROW LEVEL SECURITY;
-- Admins can view all invitations
DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE tablename = 'invitations' AND policyname = 'Admins can view invitations'
  ) THEN
    CREATE POLICY "Admins can view invitations"
      ON public.invitations FOR SELECT
      USING (
        EXISTS (
          SELECT 1 FROM public.profiles
          WHERE profiles.id = auth.uid() AND profiles.role = 'admin'
        )
      );
  END IF;
END $$;
-- Admins can create invitations
DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE tablename = 'invitations' AND policyname = 'Admins can insert invitations'
  ) THEN
    CREATE POLICY "Admins can insert invitations"
      ON public.invitations FOR INSERT
      WITH CHECK (
        EXISTS (
          SELECT 1 FROM public.profiles
          WHERE profiles.id = auth.uid() AND profiles.role = 'admin'
        )
      );
  END IF;
END $$;
-- Admins can update any invitation; claiming user can update their own claimed row
DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE tablename = 'invitations' AND policyname = 'Admins and claimants can update invitations'
  ) THEN
    CREATE POLICY "Admins and claimants can update invitations"
      ON public.invitations FOR UPDATE
      USING (
        EXISTS (
          SELECT 1 FROM public.profiles
          WHERE profiles.id = auth.uid() AND profiles.role = 'admin'
        )
        OR auth.uid() = claimed_by
      );
  END IF;
END $$;
-- Index: look up by code (claim flow) and by invited_by (admin dashboard)
CREATE INDEX IF NOT EXISTS idx_invitations_code ON public.invitations(code);
CREATE INDEX IF NOT EXISTS idx_invitations_invited_by ON public.invitations(invited_by);
CREATE INDEX IF NOT EXISTS idx_invitations_status ON public.invitations(status);
