-- 4. NOTIFICATIONS_LOG TABLE
-- ============================================================
-- Audit trail for every outbound SMS (Twilio) and email (Resend)
-- the system sends. Never written by end users — only service role.

CREATE TABLE IF NOT EXISTS public.notifications_log (
  id            uuid    PRIMARY KEY DEFAULT gen_random_uuid(),
  recipient_id  uuid    REFERENCES public.profiles(id) ON DELETE SET NULL,
  channel       text    NOT NULL CHECK (channel IN ('sms', 'email')),
  message_type  text    NOT NULL,
  -- message_type values: 'welcome', 'approved', 'reminder', 'announcement', 'invite'
  external_id   text,   -- Twilio MessageSID or Resend email ID
  status        text    NOT NULL DEFAULT 'sent',
  created_at    timestamptz NOT NULL DEFAULT now()
);
ALTER TABLE public.notifications_log ENABLE ROW LEVEL SECURITY;
-- Admins can read the log
DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE tablename = 'notifications_log' AND policyname = 'Admins can view notifications log'
  ) THEN
