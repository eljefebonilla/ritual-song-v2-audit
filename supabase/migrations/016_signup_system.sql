-- ============================================================
-- 2. HELPER FUNCTION: is_active_user
-- ============================================================
-- Returns true if the given uid maps to a profile with
-- status = 'active'. Used in RLS policies below.
-- SECURITY DEFINER: runs as the function owner, so it can
-- read profiles regardless of caller's RLS context.

CREATE OR REPLACE FUNCTION public.is_active_user(uid uuid)
RETURNS boolean AS $$
  SELECT EXISTS (
    SELECT 1 FROM public.profiles WHERE id = uid AND status = 'active'
  );
$$ LANGUAGE sql SECURITY DEFINER STABLE;
